void display(void) {
  for (uint8_t row = 0; row < 32; row++) {
    gpio__reset(LAT);
    gpio__reset(OE);
    for (uint8_t col = 0; col < 64; col++) {
      if (matrixbuff[row][col] & 0x1) {
        gpio__set(B1);
      } else {
        gpio__reset(B1);
      }
      if (matrixbuff[row][col] & 0x2) {
        gpio__set(G1);
      } else {
        gpio__reset(G1);
      }
      if (matrixbuff[row][col] & 0x4) {
        gpio__set(R1);
      } else {
        gpio__reset(R1);
      }
      if (matrixbuff[row][col] & 0x8) {
        gpio__set(B2);
      } else {
        gpio__reset(B2);
      }
      if (matrixbuff[row][col] & 0x10) {
        gpio__set(G2);
      } else {
        gpio__reset(G2);
      }
      if (matrixbuff[row][col] & 0x20) {
        gpio__set(R2);
      } else {
        gpio__reset(R2);
      }

      gpio__set(CLK);
      gpio__reset(CLK);
    }

    gpio__set(OE);
    gpio__set(LAT);

    gpio__reset(A);
    gpio__reset(B);
    gpio__reset(C);
    gpio__reset(D);
    gpio__reset(E);

    if (row & 0x1) {
      gpio__set(A);
    }

    if (row & 0x2) {
      gpio__set(B);
    }

    if (row & 0x4) {
      gpio__set(C);
    }

    if (row & 0x8) {
      gpio__set(D);
    }

    if (row & 0x10) {
      gpio__set(E);
    }
  }
}

