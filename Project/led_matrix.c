void displayScore(int score, int row, int col, int color) {
  int digit, zero_count = 0, flag = 0, rev_score = 0;

  if (score == 0)
    displayDigit(0, row, col, color);

  while (score != 0) {
    digit = score % 10;
    if (digit != 0)
      flag = 1;
    if (flag == 0)
      zero_count++;
    rev_score = (rev_score * 10) + digit;
    score /= 10;
  }

  while (rev_score != 0) {
    digit = rev_score % 10;
    displayDigit(digit, row, col, color);

    if (digit == 4)
      col += 6;
    else if (digit == 1)
      col += 2;
    else
      col += 5;

    rev_score /= 10;
  }

  while (zero_count != 0) {
    displayDigit(0, row, col, color);
    col += 5;
    zero_count--;
  }
}